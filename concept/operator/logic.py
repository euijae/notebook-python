# OR expression
my_bool = True or False
print(my_bool)

# AND expression
my_bool = True and False
print(my_bool)

# Not expression
my_bool = False
print(not my_bool)

'''
    Bit value - True is 1 False 0
    The Python compiler can automatically
    convert the bool to its numerical form when needed.
'''
print(10 * True)
print(10 * False)
