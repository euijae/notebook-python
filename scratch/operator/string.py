'''
    When two strings have different lengths,
    the string which comes first in the dictionary
    is said to have the smaller value.
'''

print('a' < 'b')

house = 'Gryffindor'
house_copy = house

print(house == house_copy)

new_house = "Slytherin"

print(house == new_house)
print(new_house <= house)
print(new_house >= house)

# string multiplication
print("ha" * 3)

# 'in' keyword
random_string = "This is a random string"
print('of' in random_string)
print('random' in random_string)
